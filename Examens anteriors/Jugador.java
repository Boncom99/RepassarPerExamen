
public class Jugador {
    int partidesJugades;
    int puntuacioMaxima;

    Jugador(int partidesJugades, int puntuacioMaxima) {
        this.partidesJugades = partidesJugades;
        this.puntuacioMaxima = puntuacioMaxima;
    }

    Jugador() {
        partidesJugades = 0;
        puntuacioMaxima = 0;
    }

}
